package swep5a;

public class Konvertieren {
	public static void main(String[] args) {
	double doubleVariable1 = 100 ; 
	double doubleVariable2 = 12 ; 
	int intVariable1= 100; 
	int intVariable2= 12; 
	
	System.out.println(doubleVariable1 * doubleVariable2); 
	System.out.println(intVariable1 * intVariable2); 
	System.out.println(doubleVariable1 * intVariable2);
	System.out.println(intVariable1 * doubleVariable2);
	
	System.out.println(doubleVariable1 / doubleVariable2); 
	System.out.println(intVariable1 / intVariable2); 
	System.out.println(doubleVariable1 / intVariable2);
	System.out.println(intVariable1 / doubleVariable2);
	
	}
}